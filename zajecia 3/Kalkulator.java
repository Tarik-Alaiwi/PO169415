public class Kalkulator {
    public static int l1;
    public static int l2;

    public static int dodawanie(int l1, int l2){
        return l1+l2;
    }
    public static int odejmowanie(int l1, int l2){
        return l1-l2;
    }
    public static int mnozenie(int l1, int l2){
        return l1*l2;
    }
    public static int dzielenie(int l1, int l2){
        return l1/l2;
    }
}
